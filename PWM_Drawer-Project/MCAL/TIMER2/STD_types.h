#ifndef STD_TYPES_H_
#define STD_TYPES_H_

typedef unsigned char           u8;   // unsigned integer 8 bit size
typedef unsigned int            u16;
typedef unsigned long int       u32;
typedef unsigned long long int  u64;

typedef char		   s8;
typedef int            s16;
typedef long int       s32;
typedef long long int  s64;

typedef float     f32;
typedef double    f64;



#endif /* STD_TYPES_H_ */